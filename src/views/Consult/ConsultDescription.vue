<script setup lang="ts">
import { IillnessTime, has } from "@/enums/consult";
import router from "@/router";
import { uploadImg } from "@/services/consult";
import { useConsultStore } from "@/stores/index";
import type { PartialConsult } from "@/types/consult";
import type {
  UploaderAfterRead,
  UploaderFileListItem,
} from "vant/lib/uploader/types";
import { reactive, watch, ref } from "vue";

let store = useConsultStore();

function clickLeft() {
  router.back();
}

let Time = [
  {
    label: "一周内",
    value: IillnessTime.Weekend,
  },
  {
    label: "一月内",
    value: IillnessTime.Month,
  },
  {
    label: "半年内",
    value: IillnessTime.LessYear,
  },
  {
    label: "大于半年",
    value: IillnessTime.MoreYear,
  },
];
let GoHospital = [
  {
    label: "就诊过",
    value: 1,
  },
  {
    label: "未就诊过",
    value: 0,
  },
];
let form = reactive<PartialConsult>({});
let OtherData = reactive<PartialConsult>({
  illnessTime: undefined,
  consultFlag: undefined,
  pictures: [],
});
let fileList = ref([]);
watch(
  () => OtherData.consultFlag,
  () => {
    console.log(store.params, "OtherData");
  }
);

let afterRead: UploaderAfterRead = function (item) {
  if (Array.isArray(item)) return;
  if (!item.file) return;
  item.status = "uploading";
  uploadImg(item.file)
    .then((res) => {
      item.status = "done";
      item.message = undefined;
      item.url = res.data.url;
      OtherData.pictures?.push(res.data);
    })
    .catch((e) => {
      item.status = "failed";
      item.message = "失败";
    });
};
function onDeleteImg(item: UploaderFileListItem) {
  console.log("DELETE");
}
</script>

<template>
  <div class="consult-illness-page">
    <nav-bar title="图文问诊" @click-left="clickLeft" />
    <!-- 医生提示 -->
    <div class="illness-tip van-hairline--bottom">
      <img class="img" src="@/assets/avatar-doctor.svg" />
      <div class="info">
        <p class="tit">在线医生</p>
        <p class="tip">
          请描述你的疾病或症状、是否用药、就诊经历，需要我听过什么样的帮助
        </p>
        <p class="safe">
          <cp-icon name="consult-safe" /><span>内容仅医生可见</span>
        </p>
      </div>
    </div>
    <!-- 表单 -->
    <div class="illness-form">
      <van-field
        type="textarea"
        rows="3"
        placeholder="请详细描述您的病情，病情描述不能为空"
        v-model="form.illnessDesc"
      ></van-field>

      <div class="item">
        <p>本次患病多久了？</p>
        <RadioBtn
          :options="Time"
          v-model:current="OtherData.illnessTime"
        ></RadioBtn>
      </div>
      <div class="item">
        <p>此次病情是否去医院就诊过？</p>
        <RadioBtn
          :options="GoHospital"
          v-model:current="OtherData.consultFlag"
        ></RadioBtn>
      </div>

      <div class="illness-img">
        <van-uploader
          :max-count="9"
          :max-size="5 * 1024 * 1024"
          multiple
          upload-icon="photo-o"
          v-model="fileList"
          upload-text="上传图片"
          :after-read="afterRead"
          @delete="onDeleteImg"
        >
          <template #preview-cover="{ file }">
            <p v-if="false">{{ file.name }}</p></template
          >
        </van-uploader>
        <p class="tip" v-if="fileList.length === 0">
          上传内容仅医生可见,最多9张图,最大5MB
        </p>
      </div>
    </div>
  </div>
</template>

